const DEVANAGARI_RE = /[\u0900-\u097F]/;
const HAS_LATIN_RE = /[A-Za-z]/;

const hasDevanagari = (text: string) => DEVANAGARI_RE.test(text);

// Minimal Latin->Devanagari transliteration for UI fallback.
const transliterateLatinWordToDevanagari = (word: string) => {
  const w = word.toLowerCase();
  const map: Array<[RegExp, string]> = [
    [/sh/g, 'श'],
    [/chh/g, 'छ'],
    [/ch/g, 'च'],
    [/th/g, 'थ'],
    [/dh/g, 'ध'],
    [/ph/g, 'फ'],
    [/bh/g, 'भ'],
    [/kh/g, 'ख'],
    [/gh/g, 'घ'],
    [/jh/g, 'झ'],
    [/ṭh/g, 'ठ'],
    [/ḍh/g, 'ढ'],
  ];

  const single: Record<string, string> = {
    a: 'अ',
    b: 'ब',
    c: 'क',
    d: 'द',
    e: 'ए',
    f: 'फ',
    g: 'ग',
    h: 'ह',
    i: 'इ',
    j: 'ज',
    k: 'क',
    l: 'ल',
    m: 'म',
    n: 'न',
    o: 'ओ',
    p: 'प',
    q: 'क',
    r: 'र',
    s: 'स',
    t: 'त',
    u: 'उ',
    v: 'व',
    w: 'व',
    x: 'क्स',
    y: 'य',
    z: 'ज',
  };

  let out = w;
  for (const [re, rep] of map) out = out.replace(re, rep);

  let res = '';
  for (const ch of out) {
    if (ch >= 'a' && ch <= 'z') res += single[ch] ?? ch;
    else res += ch;
  }
  return res;
};

const hiify = (text: string) => {
  if (!text) return text;
  if (hasDevanagari(text)) return text;
  if (!HAS_LATIN_RE.test(text)) return text;
  // Don't transliterate URLs/emails.
  if (/https?:\/\//i.test(text) || /\S+@\S+\.[A-Za-z]{2,}/.test(text)) return text;

  return text
    .split(/(\s+)/)
    .map((token) => {
      if (!/[A-Za-z]/.test(token)) return token;
      // Keep very short tokens like "ID", "OK" readable.
      if (token.length <= 2 && token.toUpperCase() === token) return token;
      // Strip punctuation around words.
      const m = token.match(/^([^A-Za-z]*)([A-Za-z][A-Za-z0-9-]*)([^A-Za-z]*)$/);
      if (!m) return token;
      const [, pre, core, post] = m;
      return `${pre}${transliterateLatinWordToDevanagari(core)}${post}`;
    })
    .join('');
};

